let currentLang = 'en';

function t(key) {
  return i18n[currentLang][key] || i18n['en'][key] || key;
}

function selectLanguage(lang) {
  currentLang = lang;
  
  // Try to update all static data-i18n elements
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    // Handle placeholder attributes for inputs
    if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
        el.setAttribute('placeholder', t(key));
    } else {
        el.innerText = t(key);
    }
  });

  // Specifically re-render the JS-driven parts if they are running
  if (typeof updateAllUI === 'function') {
      try {
          // Instead of a full re-render, we just let the next tick handle terminology where possible
          updateAllUI(); 
      } catch(e) {}
  }
  
  let lOvl = document.getElementById('lang-overlay'); lOvl.style.transition = 'opacity 0.6s ease'; lOvl.style.opacity = '0'; setTimeout(() => lOvl.style.display='none', 600);
  document.getElementById('app-wrapper').style.filter = 'none';
  
  // Trigger charts resize after layout is visible
  setTimeout(() => window.dispatchEvent(new Event('resize')), 100);
}

